"""Markdown PR-comment renderer for `asil scan` output.

Designed to be fed to `gh pr comment <pr> --body-file -` or
`actions/github-script` from a GitHub Action. The output is intentionally
GitHub-flavored markdown that renders well on PR conversations, not
Medium / general-purpose markdown.

Lays out:
  - A one-line gate-status badge.
  - A counts bar (critical / error / warning / note).
  - A `<details>` per severity tier with the findings, so the comment
    folds away cleanly when the list is long.

Stable across calls — the same report produces the same markdown,
suitable for `gh pr comment --edit-last` patterns.
"""

from __future__ import annotations

from asil_eval.scan import ScanFinding, ScanReport, Severity

_TIER_ORDER = [Severity.critical, Severity.error, Severity.warning, Severity.note]
_TIER_HEADLINE = {
    Severity.critical: "Critical",
    Severity.error: "Errors",
    Severity.warning: "Warnings",
    Severity.note: "Notes",
}
_TIER_ICON = {
    Severity.critical: "🚨",
    Severity.error: "❌",
    Severity.warning: "⚠️",
    Severity.note: "ℹ️",  # noqa: RUF001 — intentional info-source emoji in PR markdown
}


def to_pr_comment(report: ScanReport) -> str:
    """Render `report` as a single GitHub-flavoured markdown string."""
    badge = "✅ **passed**" if report.passed_gate else "❌ **failed**"
    counts = report.counts
    counts_line = " · ".join(
        f"{_TIER_ICON[t]} {counts[t.value]} {_TIER_HEADLINE[t].lower()}"
        for t in _TIER_ORDER
    )

    lines: list[str] = [
        "## ASIL scan",
        "",
        f"Gate `{report.gate}` — {badge}",
        "",
        counts_line,
        "",
        f"Repo: `{report.repo_key}` · "
        f"duration {report.duration_seconds:.2f}s · "
        f"{len(report.findings)} finding(s)",
        "",
    ]

    by_tier = _group_by_tier(report.findings)
    for tier in _TIER_ORDER:
        bucket = by_tier.get(tier, [])
        if not bucket:
            continue
        lines.append(f"<details><summary>{_TIER_ICON[tier]} "
                     f"{len(bucket)} {_TIER_HEADLINE[tier]}</summary>")
        lines.append("")
        for f in bucket:
            lines.extend(_render_finding(f))
        lines.append("</details>")
        lines.append("")

    lines.append("---")
    lines.append(
        "_This comment was generated by [ASIL](https://github.com/rkstlohchab/ASIL) "
        f"(`asil scan --gate {report.gate}`)._"
    )
    return "\n".join(lines).rstrip() + "\n"


def _group_by_tier(findings: list[ScanFinding]) -> dict[Severity, list[ScanFinding]]:
    out: dict[Severity, list[ScanFinding]] = {}
    for f in findings:
        out.setdefault(f.severity, []).append(f)
    return out


def _render_finding(f: ScanFinding) -> list[str]:
    """One bullet block per finding. File-link with optional line anchor
    when available; otherwise plain rule + message."""
    bullet = f"- **`{f.rule_id}`** — {f.message}"
    if f.file_path:
        anchor = f"#L{f.line}" if f.line else ""
        bullet += f"  (`{f.file_path}{anchor}`)"
    extras: list[str] = []
    if f.derivation:
        extras.append("  - _derivation:_ " + "; ".join(d for d in f.derivation if d))
    if f.extra:
        # Selected fields only — full extra dumps are noisy on GitHub.
        for k in ("caller", "callee", "boundary_name", "incident_id"):
            v = f.extra.get(k)
            if v:
                extras.append(f"  - **{k}**: `{v}`")
    return [bullet, *extras]
